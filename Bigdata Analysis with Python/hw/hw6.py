def distance(a,b):      
    d = 0
    for i in range(len(a)):
        d += (a[i]-b[i])**2 
    d = float(d)**0.5
    return d

print(distance(a=(1,2),b=(5,7)))

# def를 이용하여 두점사이 거리를 재는 함수를 생성한다.
# 먼저 입력받게 되는 인자는 a와 b로 이는 향후 튜플로 입력될 것이다.
# 거리에 대한 계산을 위해 d 라는 변수에 0을 할당하여 int형식의 자료형으로 만든다.
# 이후 for문을 사용하여 loop을 돌리는데 range(len(a))를 활용하여 입력받는 튜플에 길이만큼 반복하도록 설정하였다.

# d 에 a의 i번째 element에서 b의 i번째 element를 뺀 값을 제곱하고
# 이에 대한 연산을 +=으로 하여 len(a)만큼 루프가 돌아가는 동안 합을 하도록하였다.

# for문 이후에 저장된 d값에 0.5제곱을 하여 루트를 씌워주었다. 
# int 자료형으로 이러한 루트 연산을 받는다면 값이 제대로 도출되지 않기 때문에 
# 이에 대한 자료형을 float로 변환한 후 전개하였다.
# 최종적으로 d을 return하여 출력이 되도록 한다.

# distance함수에 전달인자로 a=(1,2),b=(5,7)의 튜플을 입력한다면 원하는 두점사이의 거리를 얻을 수 있다.


