def count(n):
    while (n>=0):
        if n>0:
            print(n)
            n -= 1
        else:
            print('zero!!')
            break

count(5)
# def를 통해 count라는 함수를 생성한다.
# 이를 위해 while문을 설정하여 원하는 조건인 n>=0까지 loop를 돌리려 하였다.

# 그 아래 if 문을 넣었는데, n>0인 조건이 만족한다면 
# n을 프린트 하고 주어진 n값에서 1씩 뺀 값을 다시 할당하는 것이다.
# 이는 각각의 print문으로 출력된 숫자이기 때문에 숫자마다 줄바꿈이 일어난다.

# 이러한 if문은 n이 0이 되었을 때 조건을 만족하지 않는다.
